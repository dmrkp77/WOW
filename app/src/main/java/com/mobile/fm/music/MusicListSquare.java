package com.mobile.fm.music;

public class MusicListSquare {
    private String image;
    private String name;

    public MusicListSquare(String image, String name) {
        this.image = image;
        this.name = name;
    }
    public MusicListSquare(){

    }

    public String getImage() {
        return image;
    }

    public String getName() {
        return name;
    }
}
